package com.sparc.pccf.wildlife.response;

public class SanctuaryResponse {
	private Integer sanctuaryId;
	private String sanctuaryName;
	
	
	public Integer getSanctuaryId() {
		return sanctuaryId;
	}
	public void setSanctuaryId(Integer sanctuaryId) {
		this.sanctuaryId = sanctuaryId;
	}
	public String getSanctuaryName() {
		return sanctuaryName;
	}
	public void setSanctuaryName(String sanctuaryName) {
		this.sanctuaryName = sanctuaryName;
	}
	
	

}
